/* eslint-disable no-unused-vars */
interface MenuKeys {
  idKeys: string[];
  nameKeys: string[];
}

type BaseMenuItem = {
  isSelected: boolean;
  isAtleastOneSubmenuSelected: boolean;
  isAccordianOpen: boolean;
  children: BaseMenuItem[];
};

type ResponseBaseMenuItem = {
  isSelected: boolean;
  children: ResponseBaseMenuItem[];
};

type MenuItem<K extends MenuKeys> = BaseMenuItem & {
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  [key: string]: any; // Dynamic keys
  children: MenuItem<K>[];
};

type ResponseMenuItem<K extends MenuKeys> = ResponseBaseMenuItem & {
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  [key: string]: any; // Dynamic keys
  children: ResponseMenuItem<K>[];
};
export const updateMenuState = <K extends MenuKeys>(
  menus: ResponseMenuItem<K>[]
): MenuItem<K>[] => {
  return menus.map((menu) => {
    const updatedChildren = updateMenuState(menu.children);

    const isAtleastOneSubmenuSelected = updatedChildren.some(
      (child) => child.isSelected || child.isAtleastOneSubmenuSelected
    );

    const areAllChildrenSelected =
      updatedChildren.length > 0 &&
      updatedChildren.every((child) => child.isSelected);

    const isSelected = menu.isSelected || areAllChildrenSelected;

    const isAccordianOpen = menu.isAccordianOpen || isAtleastOneSubmenuSelected;

    return {
      ...menu,
      isAccordianOpen,
      isSelected,
      isAtleastOneSubmenuSelected,
      children: updatedChildren,
    } as MenuItem<K>;
  });
};

